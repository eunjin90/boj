# 연속합
# DP, 연속 부분합 (카데인 알고리즘)

n = int(input())    # 수열의 길이 n 입력
arr = list(map(int, input().split()))   # n개의 정수를 입력받아 리스트로 저장 (수열)

# dp[i]는 "i번째 원소를 마지막으로 하는 연속 부분합의 최댓값"을 의미
# 길이 n짜리 dp 배열을 0으로 초기화
dp = [0] * n
dp[0] = arr[0]  # 첫 번째 값은 자기 자신이 최대 연속합이므로 그대로 저장

for i in range(1, n):   # 두 번째 원소부터 마지막까지 반복
    dp[i] = max(arr[i], dp[i-1] + arr[i])
    # 핵심 점화식:
    # 1) arr[i] → 새로 시작하는 경우 (이전 연속을 끊고 새로 시작)
    # 2) dp[i-1] + arr[i] → 이전 연속합에 현재 값을 이어붙이는 경우
    # 둘 중 더 큰 값을 dp[i]에 저장

print(max(dp))  # dp 배열 중 가장 큰 값이 전체 수열에서의 최대 연속 부분합